#!/bin/sh

echo [PL] Convert lang.txt from utf8 to cp852 ...
iconv -f utf-8 -t CP852 lang.txt > lang_cp852.txt
echo [PL] Convert users.txt from utf8 to cp852 ...
iconv -f utf-8 -t CP852 users.txt > users_cp852.txt
echo [PL] Convert engine.txt from utf8 to cp852 ...
iconv -f utf-8 -t CP852 engine.txt > engine_cp852.txt